	<p style="padding-left:3em;"><img src="img/GET-Evidence_summary_drawing.png" alt="" width="500" height="218" /></p>

	<p><strong>GET-Evidence</strong> is a collaborative research tool for understanding how genetic variants impact human traits and diseases. As such, it is subject to many sources of error, including: 
	<ul>
		<li>human error in interpretations</li>
		<li>errors or uncertainty in literature</li>
		<li>errors in automatic/computational processing</li>
	</ul></p>

	<p>GET-Evidence is <strong>not</strong> a &#8220;clinical quality&#8221; tool for diagnosis or treatment of any disease or medical condition. </p>

	<p>By using GET-Evidence you agree to our <a href="tos">Terms of Service</a>. </p>

	<h2>What is GET-Evidence for?</h2>

	<p>Whole genome sequencing results in thousands of variants which may have a functional consequence and have published literature findings which require review. Even after review, these findings need to be organized according to severity, impact, and strength of evidence to allow whole <a href="guide_reading_genome_reports">genome interpretation</a>. GET-Evidence was created to address this problem.</p>

	<p>GET-Evidence is also a public forum for forming consensus. GET-Evidence uses a &#8220;peer production&#8221; model for <a href="guide_reading_variant_reports">variant interpretation</a>: all users who log in may <a href="guide_editing">edit</a> variant interpretations. This model facilitates the updating and correction of interpretations, facilitating the creation of a consensus from various publications and interpretations. </p>

	<p>Finally, GET-Evidence is a public resource. Variant interpretations are shared freely (without copyright restriction). As such, GET-Evidence is a fully public method which others may build upon and against which private interpretations methods may be compared.</p>

	<p>GET-Evidence was developed to support work carried out by the <a href="http://www.personalgenomes.org">Personal Genome Project</a>. You can see some examples of <a href="genomes">genome reports</a> from Personal Genome Project participants and other public genomes. </p>

	<p>We also have various <a href="guides">guides</a> available for using and editing our system, including a <a href="guide_reading_genome_reports">guide to reading genome reports</a> and a <a href="guide_reading_variant_reports">guide to reading variant reports</a>.</p>

	<h2>Downloading data or source code</h2>

	<p>User-contributed variant interpretations are accepted and published under CC0 &#8212; i.e., without copyright protection. You are welcome to obtain the data either by means of the web service or by <a href="download">downloading a copy of the database</a>.</p>

	<p>The source files for genome data, along with GET-Evidence&#8217;s annotations, are provided alongside genome reports. See our <a href="upload_and_source_file_formats">file format descriptions</a> for more information on the format we use.</p>

